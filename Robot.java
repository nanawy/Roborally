package init;

//create a robot 

public class Robot {
	
	private final String name = "";
	private final Color color = null;
	
	
	
	public Color getColor() {
		return color;
	}
	public String getName() {
		return name;
	}
	

	public Robot(String name, Color color) {
		this.name = name;
		this.color = color;
		
	}
}
